#Calculadora de Área: Escreva um programa que leia a largura e a altura de uma parede em metros, calcule a sua área e a quantidade de tinta necessária para pintá-la, sabendo que cada litro de tinta pinta uma área de 2m².
Altura = float(input("Digite a Altura: "))
Largura = float(input("Digite a Largura: "))
Area = Altura * Largura
Litros = Area /2 
print(f'Voce vai precisar {Litros} de tinta.')









